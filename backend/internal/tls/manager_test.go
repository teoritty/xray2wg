package tlscfg

import (
	"context"
	"crypto/tls"
	"crypto/x509"
	"path/filepath"
	"testing"
	"time"
)

func TestAutoGenerateCreatesCertAndKey(t *testing.T) {
	dir := t.TempDir()
	cfg := Config{
		AutoCert: true,
		DataDir:  dir,
	}
	m, err := NewManager(context.Background(), cfg)
	if err != nil {
		t.Fatal(err)
	}
	defer m.Stop()
	certPath := filepath.Join(dir, "tls", "server.crt")
	keyPath := filepath.Join(dir, "tls", "server.key")
	if !fileExists(certPath) || !fileExists(keyPath) {
		t.Fatal("expected pem files")
	}
}

func TestLoadExistingCertificate(t *testing.T) {
	dir := t.TempDir()
	cfg := Config{AutoCert: true, DataDir: dir}
	m1, err := NewManager(context.Background(), cfg)
	if err != nil {
		t.Fatal(err)
	}
	m1.Stop()
	cfg2 := Config{
		CertFile: m1.cfg.CertFile,
		KeyFile:  m1.cfg.KeyFile,
		AutoCert: false,
	}
	m2, err := NewManager(context.Background(), cfg2)
	if err != nil {
		t.Fatal(err)
	}
	defer m2.Stop()
	if len(m2.cert.Certificate) == 0 {
		t.Fatal("expected loaded cert chain")
	}
}

