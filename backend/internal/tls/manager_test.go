func TestAutoGeneratedCertIncludesLoopbackIPs(t *testing.T) {
	dir := t.TempDir()
	m, err := NewManager(context.Background(), Config{AutoCert: true, DataDir: dir})
	if err != nil {
		t.Fatal(err)
	}
	defer m.Stop()
	if len(m.cert.Certificate) == 0 {
		t.Fatal("empty cert chain")
	}
	leaf, err := x509.ParseCertificate(m.cert.Certificate[0])
	if err != nil {
		t.Fatal(err)
	}
	var hasV4, hasV6 bool
	for _, ip := range leaf.IPAddresses {
		if ip.String() == "127.0.0.1" {
			hasV4 = true
		}
		if ip.String() == "::1" {
			hasV6 = true
		}
	}
	if !hasV4 || !hasV6 {
		t.Fatalf("expected 127.0.0.1 and ::1 in IP SANs, got %#v", leaf.IPAddresses)
	}
}

func TestTLSConfigMinVersionAndCipher(t *testing.T) {
	dir := t.TempDir()
	m, err := NewManager(context.Background(), Config{AutoCert: true, DataDir: dir})
	if err != nil {
		t.Fatal(err)
	}
	defer m.Stop()
	tc := m.TLSConfig()
	if tc.MinVersion != tls.VersionTLS12 {
		t.Fatalf("min version %x", tc.MinVersion)
	}
	if len(tc.CipherSuites) == 0 {
		t.Fatal("expected cipher suites")
	}
	if len(tc.CurvePreferences) == 0 {
		t.Fatal("expected curves")
	}
	_ = time.Now()
}
